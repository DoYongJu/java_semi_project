package bean;

public class Menu {
	int id;
	String nickname;
	String title;
	String content;

	public Menu() {

	}

	public Menu(int id, String nickname, String title, String content) {
		this.id = id;
		this.nickname = nickname;
		this.title = title;
		this.content = content;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

}
